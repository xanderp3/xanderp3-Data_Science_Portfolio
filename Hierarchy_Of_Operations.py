# What is the remainder when 8 is divided by 3?
# The mod (or modulus) operator can be used to answer this question.
# For example, the remainder when 8 is divided by 3 can be expressed as 8 % 3.
# THe % symbo is read as "mod".
print(8 % 3)

print(8.0 % 3)

